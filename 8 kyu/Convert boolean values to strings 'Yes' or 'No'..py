# Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.
# -----------------------------------------------------------

def bool_to_word(boolean):
    if boolean == True:
        return('Yes')
    if boolean == False:
        return('No')

# or

def bool_to_word(boolean):
    return 'Yes' if boolean == True else 'No'

