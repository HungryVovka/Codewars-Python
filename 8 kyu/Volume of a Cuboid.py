# Bob needs a fast way to calculate the volume of a cuboid with three values: the length, width and height of the cuboid. Write a function to help 
# Bob with this calculation.
# -----------------------------------------------------------

def getVolumeOfCubiod(length, width, height):
    return(length*width*height)

